public class Constants {

    public static void main(String[] args){

        //상수(constants, final variables)
        //한 번 값이 할당 => 재할당이 불가능
        //전체 대문자, 단어간 구분은 _
        //공식에 final 사용

        final double PI = 3.14;
        final double PI_VALUE = 3.14;
        System.out.println(PI);

        // PI = 3.141592;  -> 재할당 불가능하기에 에러 뜸

    }

}
